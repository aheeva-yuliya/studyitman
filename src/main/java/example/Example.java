package example;

/**
 * Example.
 */
public class Example {

    /**
     * Returns sum of ints.
     *
     * @cpu O(1)
     * @ram O(1)
     *
     * @param a int argument
     * @param b int argument
     * @return the sum
     */
    public static int sum(int a, int b) {
        return a + b;
    }
}
